-- ---------------------------------------------------------------------------
-- 3. 유저 분석 (정의 / 결과 / 차트 시리즈 / 표 행)
-- ---------------------------------------------------------------------------
CREATE TABLE public.user_analyses (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id bigint NOT NULL REFERENCES public.users (id) ON DELETE CASCADE,
  analysis_type text NOT NULL,
  title text NOT NULL,
  source_category text,
  dataset_name text,
  metric_name text,
  aggregation_method text,
  group_by_field text,
  filter_json jsonb,
  chart_type text,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE INDEX idx_user_analyses_user_id ON public.user_analyses (user_id);

CREATE TRIGGER trg_user_analyses_updated_at
  BEFORE UPDATE ON public.user_analyses
  FOR EACH ROW EXECUTE PROCEDURE public.set_updated_at();

CREATE TABLE public.user_analysis_results (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  analysis_id bigint NOT NULL REFERENCES public.user_analyses (id) ON DELETE CASCADE,
  result_version integer NOT NULL DEFAULT 1,
