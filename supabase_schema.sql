);

CREATE INDEX idx_user_analysis_results_analysis_id ON public.user_analysis_results (analysis_id);

CREATE TABLE public.user_analysis_chart_series (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  analysis_result_id bigint NOT NULL REFERENCES public.user_analysis_results (id) ON DELETE CASCADE,
