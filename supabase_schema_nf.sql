CREATE INDEX idx_user_analysis_filters_analysis ON public.user_analysis_filters (analysis_id);

CREATE TABLE public.user_analysis_results (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  analysis_id bigint NOT NULL REFERENCES public.user_analyses (id) ON DELETE CASCADE,
  result_version integer NOT NULL DEFAULT 1,
