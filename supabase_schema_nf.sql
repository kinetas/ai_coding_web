CREATE TABLE public.forecast_runs (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  analysis_id bigint NOT NULL REFERENCES public.user_analyses (id) ON DELETE CASCADE,
  model_type_code text NOT NULL REFERENCES public.forecast_model_types (code),
  train_start_date date,
  train_end_date date,
  prediction_start_date date,
  prediction_end_date date,
  created_at timestamptz NOT NULL DEFAULT now()
);

CREATE INDEX idx_forecast_runs_analysis ON public.forecast_runs (analysis_id);

CREATE TABLE public.forecast_metric_entries (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  forecast_run_id bigint NOT NULL REFERENCES public.forecast_runs (id) ON DELETE CASCADE,
  metric_key text NOT NULL,
  metric_value numeric NOT NULL,
  UNIQUE (forecast_run_id, metric_key)
);

CREATE TABLE public.forecast_points (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  forecast_run_id bigint NOT NULL REFERENCES public.forecast_runs (id) ON DELETE CASCADE,
  target_date date NOT NULL,
  actual_value numeric,
  predicted_value numeric,
  lower_bound numeric,
  upper_bound numeric
);

CREATE INDEX idx_forecast_points_run ON public.forecast_points (forecast_run_id);

