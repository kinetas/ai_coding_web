CREATE TABLE public.wordcloud_region_scopes (
  code text PRIMARY KEY,
  display_name text
);

CREATE TABLE public.wordcloud_categories (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  code text NOT NULL UNIQUE,
  display_name text
);

CREATE TABLE public.wordcloud_subcategories (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  category_id bigint NOT NULL REFERENCES public.wordcloud_categories (id) ON DELETE CASCADE,
