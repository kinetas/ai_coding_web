-- ---------------------------------------------------------------------------
-- 데이터셋 · 지표 (metrics: dataset_code + code 복합 PK)
-- ---------------------------------------------------------------------------
CREATE TABLE public.datasets (
  code text PRIMARY KEY,
  source_category_code text NOT NULL REFERENCES public.source_categories (code),
  display_name text
);

CREATE TABLE public.metrics (
  dataset_code text NOT NULL REFERENCES public.datasets (code) ON DELETE CASCADE,
  code text NOT NULL,
  default_label text,
  PRIMARY KEY (dataset_code, code)
);

-- ---------------------------------------------------------------------------
-- 지역
-- ---------------------------------------------------------------------------
CREATE TABLE public.regions (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  parent_region_id bigint REFERENCES public.regions (id) ON DELETE SET NULL,
  level smallint NOT NULL DEFAULT 1,
  name text NOT NULL,
  admin_code text UNIQUE
);

CREATE INDEX idx_regions_parent ON public.regions (parent_region_id);

-- ---------------------------------------------------------------------------
-- 유저 · 인증
-- ---------------------------------------------------------------------------
CREATE TABLE public.users (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  email text NOT NULL UNIQUE,
  nickname text,
  password_hash text NOT NULL,
  status_code text NOT NULL REFERENCES public.account_statuses (code),
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE INDEX idx_users_status ON public.users (status_code);

CREATE TRIGGER trg_users_updated_at
  BEFORE UPDATE ON public.users
  FOR EACH ROW EXECUTE PROCEDURE public.set_updated_at();

CREATE TABLE public.auth_sessions (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id bigint NOT NULL REFERENCES public.users (id) ON DELETE CASCADE,
  token_hash text NOT NULL UNIQUE,
  created_at timestamptz NOT NULL DEFAULT now(),
  expires_at timestamptz NOT NULL
);

CREATE INDEX idx_auth_sessions_user_id ON public.auth_sessions (user_id);
CREATE INDEX idx_auth_sessions_expires_at ON public.auth_sessions (expires_at);

