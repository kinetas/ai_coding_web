);

CREATE TABLE public.keyword_terms (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  term_text text NOT NULL,
  category_id bigint NOT NULL REFERENCES public.wordcloud_categories (id) ON DELETE RESTRICT,
  subcategory_id bigint REFERENCES public.wordcloud_subcategories (id) ON DELETE SET NULL,
  wc_region_code text NOT NULL REFERENCES public.wordcloud_region_scopes (code)
);

CREATE INDEX idx_keyword_terms_category ON public.keyword_terms (category_id);
CREATE INDEX idx_keyword_terms_region ON public.keyword_terms (wc_region_code);

CREATE TABLE public.keyword_events (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  keyword_term_id bigint NOT NULL REFERENCES public.keyword_terms (id) ON DELETE CASCADE,
  source_code text NOT NULL REFERENCES public.etl_sources (code),
  event_date date,
  count_value integer NOT NULL DEFAULT 0
);

CREATE INDEX idx_keyword_events_term ON public.keyword_events (keyword_term_id);
CREATE INDEX idx_keyword_events_date ON public.keyword_events (event_date);

CREATE TABLE public.wordcloud_agg (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  keyword_term_id bigint NOT NULL REFERENCES public.keyword_terms (id) ON DELETE CASCADE,
  aggregation_date date,
  count_value integer NOT NULL DEFAULT 0,
  weight numeric NOT NULL DEFAULT 0,
  UNIQUE (keyword_term_id, aggregation_date)
);

CREATE INDEX idx_wordcloud_agg_date ON public.wordcloud_agg (aggregation_date);

-- 빠른 조회용 캐시 (data_NF.md: 정규형 완화)
