);

CREATE TABLE public.source_categories (
  code text PRIMARY KEY,
  display_name text
);

CREATE TABLE public.analysis_types (
  code text PRIMARY KEY,
  display_name text
);

CREATE TABLE public.chart_types (
  code text PRIMARY KEY,
  display_name text
);

CREATE TABLE public.aggregation_methods (
  code text PRIMARY KEY,
  display_name text
);

CREATE TABLE public.property_types (
  code text PRIMARY KEY,
  display_name text
);

CREATE TABLE public.etl_statuses (
  code text PRIMARY KEY,
  display_name text
