CREATE TABLE public.user_analyses (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id bigint NOT NULL REFERENCES public.users (id) ON DELETE CASCADE,
  analysis_type_code text NOT NULL REFERENCES public.analysis_types (code),
  title text NOT NULL,
  dataset_code text NOT NULL REFERENCES public.datasets (code),
  metric_code text NOT NULL,
  aggregation_method_code text NOT NULL REFERENCES public.aggregation_methods (code),
  group_by_field text,
  chart_type_code text NOT NULL REFERENCES public.chart_types (code),
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now(),
  FOREIGN KEY (dataset_code, metric_code) REFERENCES public.metrics (dataset_code, code)
);

CREATE INDEX idx_user_analyses_user_id ON public.user_analyses (user_id);

CREATE TRIGGER trg_user_analyses_updated_at
  BEFORE UPDATE ON public.user_analyses
  FOR EACH ROW EXECUTE PROCEDURE public.set_updated_at();

CREATE TABLE public.user_analysis_filters (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  analysis_id bigint NOT NULL REFERENCES public.user_analyses (id) ON DELETE CASCADE,
  field_code text NOT NULL,
  operator text NOT NULL,
  value_text text,
  value_num numeric,
  sort_order integer NOT NULL DEFAULT 0
